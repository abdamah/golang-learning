package main

import "fmt"

func main() {
	var isStudent bool = true
	var age int = 20
	var height float64 = 1.75
	var name string = "Abdallah"

	fmt.Println("Student:", isStudent)
	fmt.Println("Age:", age)
	fmt.Println("Height:", height)
	fmt.Println("Name:", name)
}
